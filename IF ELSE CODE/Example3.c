// Example 3 - Checking if a number is between two values
// if-else statement to check if num is between 0 and 10.

#include <stdio.h>
int main()
{
    int num = 11;
    if (num >= 0 && num <= 10)
    {
        printf("The number is between 0 and 10\n");
    }
    else
    {
        printf("The number is not between 0 and 10");
    }
    return 0;
}